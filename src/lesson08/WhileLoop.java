package lesson08;

public class WhileLoop {
    public static void main(String[] args) {

        int num=1;
        while (num<=5)
        {
            System.out.println(num);
            num++;
        }
        int num2=100;
        while (num2>=90) {
            System.out.println(num2);
            num2--;
        }

    }
}
